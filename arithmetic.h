#define ARITHMETIC_H_
#ifdef ARITHMETIC_H_

namespace arithemtic {

class Arithemtic
{

template <class T>
class T add(T val_1, T val_2) 
{
    return val_1 + val_2;
}

template <class T>
class T subtract(T val_1, T val_2) 
{
    return val_1 - val_2;
}

template <class T>
class T multiply(T val_1, T val_2) 
{
    return val_1 * val_2;
}

template <class T>
class T divide(T val_1, T val_2) 
{
    return val_1 / val_2;
}

};

}//arithemtic
#endif //ARITHMETIC_H_
